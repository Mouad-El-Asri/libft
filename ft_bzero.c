#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*x;

	i = 0;
	x = (unsigned char *)s;
	while (n > i)
	{
		x[i] = 0;
		i++;
	}
	s = x;
}
